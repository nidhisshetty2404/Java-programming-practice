package Module7;

public class TimeComplexity {
    public static void main(String[] args) {

        System.out.println("Time Complexity Comparison");
        System.out.println("---------------------------");

        System.out.println("Linear Search:");
        System.out.println("Best Case: O(1)");
        System.out.println("Average Case: O(n)");
        System.out.println("Worst Case: O(n)");

        System.out.println();

        System.out.println("Binary Search:");
        System.out.println("Best Case: O(1)");
        System.out.println("Average Case: O(log n)");
        System.out.println("Worst Case: O(log n)");

        System.out.println();
        System.out.println("Note: Binary Search works only on sorted arrays.");
    }
}

